// Flatten the modular css/*.css into a single portable lab.css, in two
// forms:
//   - flattenRethemable: real CSS concatenated, the 6 seed values substituted,
//     calc()/var() preserved (auto-tracks tokens.css, cannot drift).
//   - flattenStatic: same concat, seeds block removed, every derived color
//     declaration rewritten to its AA-adjusted hsl() literal; non-color lines
//     verbatim. No calc(), no --nc-seed-*.
//
// Tailwind is intentionally excluded (app-only utility layer).

import { hslToString, type DerivedTheme, type HSL, deriveAll } from "./derive";
import { buildAdjustedThemes } from "./wcag";

// Raw CSS chunks, imported verbatim by Vite. Order mirrors lab.css
// minus the Tailwind lines (@import "tailwindcss" and lab.tailwind.css).
import seedsRaw from "../../css/lab.seeds.css?raw";
import tokensRaw from "../../css/lab.tokens.css?raw";
import fontsRaw from "../../css/lab.fonts.css?raw";
import resetRaw from "../../css/lab.reset.css?raw";
import buttonsRaw from "../../css/lab.buttons.css?raw";
import consoleRaw from "../../css/lab.console.css?raw";
import controlsRaw from "../../css/lab.controls.css?raw";
import datadisplayRaw from "../../css/lab.datadisplay.css?raw";
import decorationRaw from "../../css/lab.decoration.css?raw";
import diagramRaw from "../../css/lab.diagram.css?raw";
import feedbackRaw from "../../css/lab.feedback.css?raw";
import inputsRaw from "../../css/lab.inputs.css?raw";
import instrumentRaw from "../../css/lab.instrument.css?raw";
